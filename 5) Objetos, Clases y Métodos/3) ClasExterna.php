<?php
	class ClaseMate {
		
		public function __construct() 
		{}
		
		function suma($num1, $num2) {
			$resultado = $num1 + $num2;
			return $resultado;
		}
		
		function resta($num1, $num2) {
			$resultado = $num1 - $num2;
			return $resultado;
		}
		
		function multiplicacion($num1, $num2) {
			$resultado = $num1 * $num2;
			return $resultado;
		}
		
		function division($num1, $num2) {
			$resultado = $num1 / $num2;
			return $resultado;
		}
		
	}//Fin MiClase
?>
